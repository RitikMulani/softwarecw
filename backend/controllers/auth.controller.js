import bcrypt from 'bcryptjs';
import jwt from 'jsonwebtoken';
import { v4 as uuidv4 } from 'uuid';
import db from '../config/database.js';
import dotenv from 'dotenv';

dotenv.config();

/**
 * Generate access and refresh tokens
 */
function generateTokens(user) {
  const jwtSecret = process.env.JWT_SECRET || 'your-secret-key-change-in-production';
  const jwtRefreshSecret = process.env.JWT_REFRESH_SECRET || jwtSecret;
  
  const accessToken = jwt.sign(
    {
      userId: user.id,
      email: user.email,
      role: user.user_type,
    },
    jwtSecret,
    { expiresIn: process.env.JWT_EXPIRES_IN || '7d' }
  );

  const refreshToken = jwt.sign(
    {
      userId: user.id,
      email: user.email,
    },
    jwtRefreshSecret,
    { expiresIn: process.env.JWT_REFRESH_EXPIRES_IN || '30d' }
  );

  return { accessToken, refreshToken };
}

/**
 * Register a new user
 */
export async function register(req, res) {
  try {
    const { email, password, name, role = 'user' } = req.body;

    // Check if user exists
    const [existing] = await db.query('SELECT id FROM users WHERE email = ?', [email]);

    if (existing.length > 0) {
      return res.status(400).json({ message: 'User already exists with this email' });
    }

    // Hash password
    const hashedPassword = await bcrypt.hash(password, 10);

    // Map role to valid user_type (patient, doctor, or admin)
    const userType = role === 'doctor' ? 'doctor' : 'patient';

    // Insert user (id will be auto-generated by SERIAL)
    const [result] = await db.query(
      `INSERT INTO users (email, password, full_name, user_type, created_at, updated_at) 
       VALUES (?, ?, ?, ?, NOW(), NOW())`,
      [email, hashedPassword, name, userType]
    );

    res.status(201).json({
      message: 'User registered successfully',
      userId: result[0]?.id || 'registered',
    });
  } catch (error) {
    res.status(500).json({ message: 'Error registering user', error: error.message });
  }
}

/**
 * Login user
 */
export async function login(req, res) {
  try {
    const { email, password } = req.body;

    // Find user
    const [users] = await db.query('SELECT * FROM users WHERE email = ?', [email]);

    if (users.length === 0) {
      return res.status(401).json({ message: 'Invalid email or password' });
    }

    const user = users[0];

    // Verify password
    const isValid = await bcrypt.compare(password, user.password);

    if (!isValid) {
      return res.status(401).json({ message: 'Invalid email or password' });
    }

    // Generate tokens
    const { accessToken, refreshToken } = generateTokens(user);

    // Store refresh token (skip if table doesn't exist)
    try {
      const tokenId = uuidv4();
      const expiresAt = new Date();
      expiresAt.setDate(expiresAt.getDate() + 7);

      await db.query(
        `INSERT INTO refresh_tokens (id, user_id, token, expires_at, created_at) 
         VALUES (?, ?, ?, ?, NOW())`,
        [tokenId, user.id, refreshToken, expiresAt]
      );
    } catch (err) {
      // Table might not exist, continue anyway
    }

    // Return tokens and user info
    res.json({
      message: 'Login successful',
      accessToken,
      refreshToken,
      user: {
        id: user.id,
        email: user.email,
        name: user.full_name,
        role: user.user_type,
      },
    });
  } catch (error) {
    res.status(500).json({ message: 'Error logging in', error: error.message });
  }
}

/**
 * Refresh access token
 */
export async function refresh(req, res) {
  try {
    const { refreshToken } = req.body;

    if (!refreshToken) {
      return res.status(401).json({ message: 'Refresh token required' });
    }

    // Verify token
    let decoded;
    try {
      decoded = jwt.verify(refreshToken, process.env.JWT_REFRESH_SECRET);
    } catch (error) {
      return res.status(403).json({ message: 'Invalid refresh token' });
    }

    // Check if token exists and is not revoked
    const [tokens] = await db.query(
      `SELECT * FROM refresh_tokens 
       WHERE token = ? AND user_id = ? AND revoked = FALSE AND expires_at > NOW()`,
      [refreshToken, decoded.userId]
    );

    if (tokens.length === 0) {
      return res.status(403).json({ message: 'Invalid or expired refresh token' });
    }

    // Get user
    const [users] = await db.query('SELECT * FROM users WHERE id = ?', [decoded.userId]);

    if (users.length === 0) {
      return res.status(404).json({ message: 'User not found' });
    }

    // Generate new tokens
    const { accessToken, refreshToken: newRefreshToken } = generateTokens(users[0]);

    // Revoke old refresh token
    await db.query('UPDATE refresh_tokens SET revoked = TRUE WHERE id = ?', [tokens[0].id]);

    // Store new refresh token
    const tokenId = uuidv4();
    const expiresAt = new Date();
    expiresAt.setDate(expiresAt.getDate() + 7);

    await db.query(
      `INSERT INTO refresh_tokens (id, user_id, token, expires_at, created_at) 
       VALUES (?, ?, ?, ?, NOW())`,
      [tokenId, decoded.userId, newRefreshToken, expiresAt]
    );

    res.json({
      accessToken,
      refreshToken: newRefreshToken,
    });
  } catch (error) {
    res.status(500).json({ message: 'Error refreshing token', error: error.message });
  }
}

/**
 * Get current user profile
 */
export async function me(req, res) {
  try {
    const [users] = await db.query(
      `SELECT id, email, full_name, user_type, phone, date_of_birth, gender, address, created_at, updated_at 
       FROM users WHERE id = ?`,
      [req.user.userId]
    );

    if (users.length === 0) {
      return res.status(404).json({ message: 'User not found' });
    }

    const user = users[0];

    // If patient, get patient details
    if (user.user_type === 'patient') {
      const [patients] = await db.query(
        'SELECT blood_group, allergies, chronic_conditions, emergency_contact, emergency_phone FROM patients WHERE user_id = ?',
        [user.id]
      );
      user.patientDetails = patients[0] || {
        blood_group: null,
        allergies: null,
        chronic_conditions: null,
        emergency_contact: null,
        emergency_phone: null
      };
    } else if (user.user_type === 'doctor') {
      // If doctor, get doctor details
      const [doctors] = await db.query(
        'SELECT specialization, qualification, experience_years, license_number, consultation_fee, available_days, available_hours FROM doctors WHERE user_id = ?',
        [user.id]
      );
      user.doctorDetails = doctors[0] || {
        specialization: null,
        qualification: null,
        experience_years: null,
        license_number: null,
        consultation_fee: null,
        available_days: null,
        available_hours: null
      };
    }

    res.json({
      user
    });
  } catch (error) {
    res.status(500).json({ message: 'Error fetching profile', error: error.message });
  }
}

/**
 * Logout (revoke refresh token)
 */
export async function logout(req, res) {
  try {
    const { refreshToken } = req.body;

    if (refreshToken) {
      await db.query('UPDATE refresh_tokens SET revoked = TRUE WHERE token = ?', [refreshToken]);
    }

    res.json({ message: 'Logged out successfully' });
  } catch (error) {

    res.status(500).json({ message: 'Error logging out', error: error.message });
  }
}

/**
 * Update user profile
 */
export async function updateProfile(req, res) {
  try {
    const userId = req.user.userId;
    const {
      full_name,
      phone,
      date_of_birth,
      gender,
      address,
      // Patient fields
      blood_group,
      allergies,
      chronic_conditions,
      emergency_contact,
      emergency_phone,
      // Doctor fields
      specialization,
      qualification,
      experience_years,
      consultation_fee,
      available_days,
      available_hours
    } = req.body;

    // Get user type first
    const [users] = await db.query('SELECT user_type FROM users WHERE id = ?', [userId]);
    
    if (!users || users.length === 0) {
      return res.status(404).json({ message: 'User not found' });
    }

    const userType = users[0].user_type;

    // Update users table
    const updates = [];
    const values = [];

    if (full_name) {
      updates.push('full_name = ?');
      values.push(full_name);
    }
    if (phone) {
      updates.push('phone = ?');
      values.push(phone);
    }
    if (date_of_birth) {
      updates.push('date_of_birth = ?');
      values.push(date_of_birth);
    }
    if (gender) {
      updates.push('gender = ?');
      values.push(gender);
    }
    if (address) {
      updates.push('address = ?');
      values.push(address);
    }

    if (updates.length > 0) {
      updates.push('updated_at = CURRENT_TIMESTAMP');
      values.push(userId);
      await db.query(`UPDATE users SET ${updates.join(', ')} WHERE id = ?`, values);
    }

    // Handle patient-specific fields
    if (userType === 'patient') {
      // Check if patient record exists
      const [existingPatient] = await db.query(
        'SELECT id FROM patients WHERE user_id = ?',
        [userId]
      );

      if (existingPatient && existingPatient.length > 0) {
        // Update existing patient record
        const patientUpdates = [];
        const patientValues = [];

        if (blood_group !== undefined) {
          patientUpdates.push('blood_group = ?');
          patientValues.push(blood_group || null);
        }
        if (allergies !== undefined) {
          patientUpdates.push('allergies = ?');
          patientValues.push(allergies || null);
        }
        if (chronic_conditions !== undefined) {
          patientUpdates.push('chronic_conditions = ?');
          patientValues.push(chronic_conditions || null);
        }
        if (emergency_contact !== undefined) {
          patientUpdates.push('emergency_contact = ?');
          patientValues.push(emergency_contact || null);
        }
        if (emergency_phone !== undefined) {
          patientUpdates.push('emergency_phone = ?');
          patientValues.push(emergency_phone || null);
        }

        if (patientUpdates.length > 0) {
          patientValues.push(userId);
          await db.query(
            `UPDATE patients SET ${patientUpdates.join(', ')} WHERE user_id = ?`,
            patientValues
          );
        }
      } else {
        // Create new patient record
        await db.query(
          `INSERT INTO patients (user_id, blood_group, allergies, chronic_conditions, emergency_contact, emergency_phone) 
           VALUES (?, ?, ?, ?, ?, ?)`,
          [userId, blood_group || null, allergies || null, chronic_conditions || null, emergency_contact || null, emergency_phone || null]
        );
      }
    } else if (userType === 'doctor') {
      // Check if doctor record exists
      const [existingDoctor] = await db.query(
        'SELECT id FROM doctors WHERE user_id = ?',
        [userId]
      );

      if (existingDoctor && existingDoctor.length > 0) {
        // Update existing doctor record
        const doctorUpdates = [];
        const doctorValues = [];

        if (specialization !== undefined) {
          doctorUpdates.push('specialization = ?');
          doctorValues.push(specialization || null);
        }
        if (qualification !== undefined) {
          doctorUpdates.push('qualification = ?');
          doctorValues.push(qualification || null);
        }
        if (experience_years !== undefined) {
          doctorUpdates.push('experience_years = ?');
          doctorValues.push(experience_years || null);
        }
        if (consultation_fee !== undefined) {
          doctorUpdates.push('consultation_fee = ?');
          doctorValues.push(consultation_fee || null);
        }
        if (available_days !== undefined) {
          doctorUpdates.push('available_days = ?');
          doctorValues.push(available_days || null);
        }
        if (available_hours !== undefined) {
          doctorUpdates.push('available_hours = ?');
          doctorValues.push(available_hours || null);
        }

        if (doctorUpdates.length > 0) {
          doctorValues.push(userId);
          await db.query(
            `UPDATE doctors SET ${doctorUpdates.join(', ')} WHERE user_id = ?`,
            doctorValues
          );
        }
      } else {
        // Create new doctor record
        await db.query(
          `INSERT INTO doctors (user_id, specialization, qualification, experience_years, consultation_fee, available_days, available_hours) 
           VALUES (?, ?, ?, ?, ?, ?, ?)`,
          [userId, specialization || null, qualification || null, experience_years || null, consultation_fee || null, available_days || null, available_hours || null]
        );
      }
    }

    res.json({ message: 'Profile updated successfully' });
  } catch (error) {
    res.status(500).json({ message: 'Error updating profile', error: error.message });
  }
}

